package books_java_leetcode.immutable.immutableClass;

public class Radius {
    private int start;
    private int end;

    public Radius(int start, int end) {
    }

    public int getStart() {
        return start;
    }

    public void setStart(int start) {
        this.start = start;
    }

    public int getEnd() {
        return end;
    }

    public void setEnd(int end) {
        this.end = end;
    }
}
